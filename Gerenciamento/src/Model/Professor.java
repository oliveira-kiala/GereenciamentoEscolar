package Model;

public class Professor {
    
    private String nome;
    private String titulo;
    private String numMatricula;
    private String cpf;
    private String rg;
    private String discipl;
    private String cep;
    private String cidade_Munic;
    private String estado;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getNumMatricula() {
        return numMatricula;
    }

    public void setNumMatricula(String numMatricula) {
        this.numMatricula = numMatricula;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getRg() {
        return rg;
    }

    public void setRg(String rg) {
        this.rg = rg;
    }

    public String getDiscipl() {
        return discipl;
    }

    public void setDiscipl(String discipl) {
        this.discipl = discipl;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public String getCidade_Munic() {
        return cidade_Munic;
    }

    public void setCidade_Munic(String cidade_Munic) {
        this.cidade_Munic = cidade_Munic;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    
    
}
